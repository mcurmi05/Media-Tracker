// Builds api/_data/popularTitles.ts: a compact index of well-known movie/TV
// titles used by the search recovery path to spell-correct queries TMDB
// itself can't match (e.g. "intrastellar" -> "interstellar").
//
// Usage: node scripts/build-title-index.mjs   (needs TMDB_API_KEY in .env)
// Rerun occasionally so newly popular titles join the index.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const key = (
  process.env.TMDB_API_KEY ||
  readFileSync(join(root, ".env"), "utf8").match(/TMDB_API_KEY=(.*)/)?.[1] ||
  ""
).trim();
if (!key) {
  console.error("TMDB_API_KEY not found");
  process.exit(1);
}

// Same normalization as api/tmdb.ts so index lookups compare like for like.
function normalize(s) {
  return String(s || "")
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[^\p{L}\p{N}\s]/gu, "")
    .replace(/\s+/g, " ")
    .trim();
}

const LISTS = [
  "/movie/popular",
  "/movie/top_rated",
  "/tv/popular",
  "/tv/top_rated",
];
const PAGES_PER_LIST = 100;

async function fetchPage(path, page) {
  const url = new URL(`https://api.themoviedb.org/3${path}`);
  url.searchParams.set("page", String(page));
  const res = await fetch(url, {
    headers: { Authorization: `Bearer ${key}` },
  });
  if (!res.ok) return { results: [] };
  return res.json();
}

// normalized title -> max popularity seen
const titles = new Map();
function add(title, popularity) {
  const t = normalize(title);
  if (!t || t.length < 2) return;
  const pop = Math.round((popularity || 0) * 10) / 10;
  if (!titles.has(t) || titles.get(t) < pop) titles.set(t, pop);
}

const BATCH = 20;
for (const list of LISTS) {
  for (let start = 1; start <= PAGES_PER_LIST; start += BATCH) {
    const pages = [];
    for (let p = start; p < start + BATCH && p <= PAGES_PER_LIST; p++) {
      pages.push(fetchPage(list, p));
    }
    for (const data of await Promise.all(pages)) {
      for (const it of data.results || []) {
        add(it.title || it.name, it.popularity);
        add(it.original_title || it.original_name, it.popularity);
      }
    }
  }
  console.log(`${list}: index now ${titles.size} titles`);
}

const rows = [...titles.entries()].sort((a, b) => b[1] - a[1]);
const out = `// Generated by scripts/build-title-index.mjs - do not edit by hand.
// [normalized title, tmdb popularity], used for search spell-correction.
const POPULAR_TITLES: [string, number][] = ${JSON.stringify(rows)};
export default POPULAR_TITLES;
`;
mkdirSync(join(root, "api/_data"), { recursive: true });
writeFileSync(join(root, "api/_data/popularTitles.ts"), out);
console.log(`Wrote ${rows.length} titles to api/_data/popularTitles.ts`);
